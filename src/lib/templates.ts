export const FINCERT_TEMPLATE = `Получен срочный запрос от ФинЦЕРТ

{{TransactionID}}

В запросе необходимы следующие данные:

1. Торговое наименование проекта
2. Вся информация о клиенте
3. Информация, что за товар
4. Обращался ли клиент в поддержку сервиса, если да, то необходимы скриншоты переписки
5. Информация о том, что товар был получен

просьба ответить на него до {{Deadline}} единым файлом в формате PDF`;

export function generateMessageText(type: string, transactionId: string, deadline: string): string {
  const isFincert = type.toLowerCase().includes('финцерт') || type.toLowerCase().includes('банк');
  const template = isFincert ? FINCERT_TEMPLATE : CHARGEBACK_TEMPLATE;
  
  return template
    .replace('{{TransactionID}}', transactionId || '')
    .replace('{{Deadline}}', deadline || '');
}
